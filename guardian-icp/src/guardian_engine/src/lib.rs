#[init]
fn init() {
    // Start the 30-second heartbeat timer
    ic_cdk_timers::set_timer_interval(
        std::time::Duration::from_secs(30),
        timer_tick,
    );
    IS_RUNNING.with(|r| *r.borrow_mut() = true);
    ic_cdk::println!("Guardian Engine initialised");
}

// ---------------------------------------------------------------------------
// Query endpoints
// ---------------------------------------------------------------------------

#[query]
fn get_health() -> EngineHealthStatus {
    let cycle_balance = api::canister_balance();
    let last_tick = get_last_tick();
    let is_running = IS_RUNNING.with(|r| *r.borrow());
    let watermark_count = WATERMARKS.with(|w| w.borrow().len());

    EngineHealthStatus {
        cycle_balance,
        last_tick,
        is_running,
        watermark_count,
    }
}


        fn make_out_event(amount: u64, counterparty: Principal, ts: u64) -> UnifiedEvent {
            UnifiedEvent {
                chain: "ICP".to_string(),
                timestamp: ts,
                direction: Direction::Out,
                amount_e8s: amount,
                counterparty,
                tx_id: format!("tx-{}", ts),
            }
        }

        fn make_in_event(amount: u64, ts: u64) -> UnifiedEvent {
            UnifiedEvent {
                chain: "ICP".to_string(),
                timestamp: ts,
                direction: Direction::In,
                amount_e8s: amount,
                counterparty: Principal::anonymous(),
                tx_id: format!("in-{}", ts),
            }
        }


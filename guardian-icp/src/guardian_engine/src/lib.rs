// ---------------------------------------------------------------------------
// Stable storage (thread-local)
// ---------------------------------------------------------------------------

/// Stable key used for the single-slot "last_tick" record.
const LAST_TICK_KEY: &str = "__last_tick__";
/// Stable key for config canister principal.
const CONFIG_CANISTER_KEY: &str = "__config_canister__";

thread_local! {
    /// Watermarks: (principal || chain_discriminant) → Watermark
    static WATERMARKS: RefCell<StableBTreeMap<WatermarkKey, Watermark, DefaultMemoryImpl>> =
        RefCell::new(StableBTreeMap::new(DefaultMemoryImpl::default()));

    /// Alert history: alert_id → AlertRecord
    static ALERTS: RefCell<StableBTreeMap<String, AlertRecord, DefaultMemoryImpl>> =
        RefCell::new(StableBTreeMap::new(DefaultMemoryImpl::default()));

    /// Single-slot metadata (last_tick, config canister id, …)
    static META: RefCell<StableBTreeMap<String, Vec<u8>, DefaultMemoryImpl>> =
        RefCell::new(StableBTreeMap::new(DefaultMemoryImpl::default()));

    /// Per-user event ring buffer: WatermarkKey (principal, ICP chain) → candid-encoded Vec<UnifiedEvent>
    /// We reuse WatermarkKey as a 30-byte principal key (chain byte = 0xFF for event store).
    static USER_EVENTS: RefCell<StableBTreeMap<WatermarkKey, Vec<u8>, DefaultMemoryImpl>> =
        RefCell::new(StableBTreeMap::new(DefaultMemoryImpl::default()));

    /// Runtime flag: has the timer been started?
    static IS_RUNNING: RefCell<bool> = RefCell::new(false);
}

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

fn get_last_tick() -> u64 {
    META.with(|m| {
        m.borrow()
            .get(&LAST_TICK_KEY.to_string())
            .and_then(|b| candid::decode_one::<LastTick>(&b).ok())
            .map(|lt| lt.value)
            .unwrap_or(0)
    })
}

fn set_last_tick(ts: u64) {
    META.with(|m| {
        let bytes = candid::encode_one(&LastTick { value: ts }).unwrap();
        m.borrow_mut().insert(LAST_TICK_KEY.to_string(), bytes);
    });
}

fn get_config_canister_id() -> Option<Principal> {
    META.with(|m| {
        m.borrow()
            .get(&CONFIG_CANISTER_KEY.to_string())
            .and_then(|b| candid::decode_one::<StoredPrincipal>(&b).ok())
            .and_then(|sp| sp.value)
    })
}

fn store_config_canister_id(id: Principal) {
    META.with(|m| {
        let bytes = candid::encode_one(&StoredPrincipal { value: Some(id) }).unwrap();
        m.borrow_mut().insert(CONFIG_CANISTER_KEY.to_string(), bytes);
    });
}

/// Cycle drain protection — panics (traps) if balance is critically low.
fn guard_cycles() {
    let balance = api::canister_balance();
    if balance < MIN_CYCLE_BALANCE {
        ic_cdk::trap(&format!(
            "Cycle drain guard: balance {} < minimum {}",
            balance, MIN_CYCLE_BALANCE
        ));
    }
}

/// Reject anonymous callers on update endpoints.
fn reject_anonymous() {
    if caller() == Principal::anonymous() {
        ic_cdk::trap("Anonymous callers are not permitted");
    }
}


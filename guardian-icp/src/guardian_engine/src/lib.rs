/// Record of an alert that was generated by the engine.
#[derive(CandidType, Deserialize, Serialize, Clone, Debug)]
pub struct AlertRecord {
    pub alert_id: String,
    pub timestamp: u64,
    pub user: Principal,
    pub rules_triggered: Vec<String>,
    pub severity: u8,
    pub status: AlertStatus,
}

impl Storable for AlertRecord {
    fn to_bytes(&self) -> Cow<[u8]> {
        Cow::Owned(candid::encode_one(self).unwrap())
    }
    fn from_bytes(bytes: Cow<[u8]>) -> Self {
        candid::decode_one(&bytes).unwrap()
    }
    const BOUND: Bound = Bound::Unbounded;
}

/// Per-chain polling watermark stored in stable memory.
#[derive(CandidType, Deserialize, Serialize, Clone, Debug, Default)]
pub struct Watermark {
    pub last_tx_id: String,
    pub last_checked: u64,
    pub block_height: u64,
}

impl Storable for Watermark {
    fn to_bytes(&self) -> Cow<[u8]> {
        Cow::Owned(candid::encode_one(self).unwrap())
    }
    fn from_bytes(bytes: Cow<[u8]>) -> Self {
        candid::decode_one(&bytes).unwrap()
    }
    const BOUND: Bound = Bound::Unbounded;
}

/// Stable-memory key for the watermark map: (principal bytes + chain discriminant).
/// We encode as a 30-byte fixed array: 29 bytes principal + 1 byte chain.
#[derive(Clone, Debug, PartialEq, Eq, PartialOrd, Ord)]
pub struct WatermarkKey(pub [u8; 30]);

impl WatermarkKey {
    pub fn new(principal: &Principal, chain: &Chain) -> Self {
        let pb = principal.as_slice();
        let mut key = [0u8; 30];
        let len = pb.len().min(29);
        key[..len].copy_from_slice(&pb[..len]);
        key[29] = chain.discriminant();
        WatermarkKey(key)
    }
}

impl Storable for WatermarkKey {
    fn to_bytes(&self) -> Cow<[u8]> {
        Cow::Owned(self.0.to_vec())
    }
    fn from_bytes(bytes: Cow<[u8]>) -> Self {
        let mut arr = [0u8; 30];
        let len = bytes.len().min(30);
        arr[..len].copy_from_slice(&bytes[..len]);
        WatermarkKey(arr)
    }
    const BOUND: Bound = Bound::Bounded { max_size: 30, is_fixed_size: true };
}


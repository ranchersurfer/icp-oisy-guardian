#[derive(CandidType, Deserialize, Serialize, Clone, Debug, PartialEq)]
pub enum Chain {
    ICP,
    CkBTC,
    CkETH,
}

impl Chain {
    /// Return a stable u8 discriminant for use as a BTreeMap key byte.
    pub fn discriminant(&self) -> u8 {
        match self {
            Chain::ICP => 0,
            Chain::CkBTC => 1,
            Chain::CkETH => 2,
        }
    }

    pub fn from_discriminant(d: u8) -> Option<Self> {
        match d {
            0 => Some(Chain::ICP),
            1 => Some(Chain::CkBTC),
            2 => Some(Chain::CkETH),
            _ => None,
        }
    }
}

#[derive(CandidType, Deserialize, Serialize, Clone, Debug, PartialEq)]
pub enum Direction {
    In,
    Out,
}

#[derive(CandidType, Deserialize, Serialize, Clone, Debug, PartialEq)]
pub enum AlertStatus {
    Pending,
    Sent,
    Failed,
}


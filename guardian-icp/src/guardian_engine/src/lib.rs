/// Store the principal of the companion guardian_config canister.
#[update]
fn set_config_canister_id(id: Principal) {
    reject_anonymous();
    guard_cycles();
    store_config_canister_id(id);
    ic_cdk::println!("Config canister id set to {:?}", id);
}

